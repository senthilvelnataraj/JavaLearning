package java_basics;

public class UnaryOperator {

	public static void main(String[] args) {
		int a=10;
		int b=-10;
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(--a);
System.out.println(~a); //a=-1+(-a)
System.out.println(~b); //b=-1+(+b)
	}

}
