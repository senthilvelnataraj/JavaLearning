package java_basics;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int num1=100;
		int num2=9;
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		System.out.println(num1%num2);
		num1++;
		System.out.println(num1);
		num1+=3;
		System.out.println(num1);
		num1*=2;
		System.out.println(num1);
		num1/=2;
		System.out.println(num1);
		num2--;
		System.out.println(num2);
		

	}

}
